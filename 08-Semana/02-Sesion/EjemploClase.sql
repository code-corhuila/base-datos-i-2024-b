CREATE DATABASE ejercicio;

use ejercicio;


CREATE TABLE Continente (continente_id INT AUTO_INCREMENT PRIMARY KEY, nombre VARCHAR(100) NOT NULL);
CREATE TABLE Pais (pais_id INT AUTO_INCREMENT PRIMARY KEY, nombre VARCHAR(100) NOT NULL, continente_id INT, FOREIGN KEY (continente_id) REFERENCES Continente(continente_id));
CREATE TABLE Departamento (departamento_id INT AUTO_INCREMENT PRIMARY KEY, nombre VARCHAR(100) NOT NULL, pais_id INT, FOREIGN KEY (pais_id) REFERENCES Pais(pais_id));
CREATE TABLE Ciudad (ciudad_id INT AUTO_INCREMENT PRIMARY KEY, nombre VARCHAR(100) NOT NULL, departamento_id INT, FOREIGN KEY (departamento_id) REFERENCES Departamento(departamento_id));
CREATE TABLE Persona (persona_id INT AUTO_INCREMENT PRIMARY KEY, nombre VARCHAR(100) NOT NULL, apellido VARCHAR(100) NOT NULL, fecha_nacimiento VARCHAR(20), ciudad_id INT, FOREIGN KEY (ciudad_id) REFERENCES Ciudad(ciudad_id));
CREATE TABLE Usuario (usuario_id INT AUTO_INCREMENT PRIMARY KEY, username VARCHAR(100) NOT NULL, password VARCHAR(100) NOT NULL, persona_id INT, FOREIGN KEY (persona_id) REFERENCES Persona(persona_id));
CREATE TABLE Rol (rol_id INT AUTO_INCREMENT PRIMARY KEY, nombre VARCHAR(100) NOT NULL);
CREATE TABLE UsuarioRol (usuario_id INT, rol_id INT, PRIMARY KEY (usuario_id, rol_id), FOREIGN KEY (usuario_id) REFERENCES Usuario(usuario_id), FOREIGN KEY (rol_id) REFERENCES Rol(rol_id));
CREATE TABLE Producto (producto_id INT AUTO_INCREMENT PRIMARY KEY, nombre VARCHAR(100) NOT NULL, precio DECIMAL(10,2));
CREATE TABLE Factura (factura_id INT AUTO_INCREMENT PRIMARY KEY, fecha DATE NOT NULL, usuario_id INT, FOREIGN KEY (usuario_id) REFERENCES Usuario(usuario_id));
CREATE TABLE DetalleFactura (detalle_id INT AUTO_INCREMENT PRIMARY KEY, factura_id INT, producto_id INT, cantidad INT, precio_unitario DECIMAL(10,2), FOREIGN KEY (factura_id) REFERENCES Factura(factura_id), FOREIGN KEY (producto_id) REFERENCES Producto(producto_id));

